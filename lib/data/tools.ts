/**
 * Check if a tool is available based on pro activation status.
 * If pro is not activated, all tools with tier "starter" or "pro" are blocked.
 */
export function isToolAvailable(tool: ToolConfig): boolean {
  if (typeof window === "undefined") return true;
  const proActive = localStorage.getItem("xalura_pro_activated") === "true";
  // Non-free tools require pro to be active
  if (tool.tier !== "free" && !proActive) {
    return false;
  }
  return true;
}
